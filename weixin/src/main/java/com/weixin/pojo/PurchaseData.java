package com.weixin.pojo;

public class PurchaseData {
	private KeyValue User;
	private KeyValue Date;
	private KeyValue CardNumber;
	private KeyValue Type;
	private KeyValue Money;
	private KeyValue Left;

	public KeyValue getUser() {
		return User;
	}

	public void setUser(KeyValue user) {
		User = user;
	}

	public KeyValue getDate() {
		return Date;
	}

	public void setDate(KeyValue date) {
		Date = date;
	}

	public KeyValue getCardNumber() {
		return CardNumber;
	}

	public void setCardNumber(KeyValue cardNumber) {
		CardNumber = cardNumber;
	}

	public KeyValue getType() {
		return Type;
	}

	public void setType(KeyValue type) {
		Type = type;
	}

	public KeyValue getMoney() {
		return Money;
	}

	public void setMoney(KeyValue money) {
		Money = money;
	}

	public KeyValue getLeft() {
		return Left;
	}

	public void setLeft(KeyValue left) {
		Left = left;
	}
}
