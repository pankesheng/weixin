package com.weixin.pojo;

public class ReturnHist {
	private String otherRefundId;
	private String returnChannelType;
	private float returnMoneyBak;
	private float returnMoneyBalance;
	private String returnOrderNo;
	private String appKey;
	public String getOtherRefundId() {
		return otherRefundId;
	}
	public void setOtherRefundId(String otherRefundId) {
		this.otherRefundId = otherRefundId;
	}
	public String getReturnChannelType() {
		return returnChannelType;
	}
	public void setReturnChannelType(String returnChannelType) {
		this.returnChannelType = returnChannelType;
	}
	public float getReturnMoneyBak() {
		return returnMoneyBak;
	}
	public void setReturnMoneyBak(float returnMoneyBak) {
		this.returnMoneyBak = returnMoneyBak;
	}
	public float getReturnMoneyBalance() {
		return returnMoneyBalance;
	}
	public void setReturnMoneyBalance(float returnMoneyBalance) {
		this.returnMoneyBalance = returnMoneyBalance;
	}
	public String getReturnOrderNo() {
		return returnOrderNo;
	}
	public void setReturnOrderNo(String returnOrderNo) {
		this.returnOrderNo = returnOrderNo;
	}
	public String getAppKey() {
		return appKey;
	}
	public void setAppKey(String appKey) {
		this.appKey = appKey;
	}
	
}
